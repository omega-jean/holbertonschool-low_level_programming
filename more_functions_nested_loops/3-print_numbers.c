#include "main.h"

/**
  * print_numbers - Print numbers from zero to nine followed by a line
  *
  *
  *  return: The absolute value of the integer
  */

void print_numbers(void)
{
int n = 48;
while (n <= 57)
{
_putchar(n);
n++;
}
_putchar('\n');
}
